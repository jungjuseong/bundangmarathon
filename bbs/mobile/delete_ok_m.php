<?
  // 라이브러리 함수 파일 인크루드
	include "../lib.php";										//기존 라이브러리
	include $_zb_path."lib_m.php";			//추가 라이브러리[모바일용]

	$getData = iconv_CP949All($HTTP_POST_VARS);
	$page = $getData[page];
	$id = $getData[id];
	$no = $getData[no];
	$select_arrange = $getData[select_arrange];
	$desc = $getData[desc];
	$page_num = $getData[page_num];
	$keyword = $getData[keyword];
	$category = $getData[category];
	$sn = $getData[sn];
	$ss = $getData[ss];
	$sc = $getData[sc];
	$mode = $getData[mode];
	$c_no = $getData[c_no];

	if(!eregi($HTTP_HOST,$HTTP_REFERER)){
		echo(iconv_UTF("정상적으로 글을 삭제하여 주시기 바랍니다."));
		exit;
	}

	if(getenv("REQUEST_METHOD") == 'GET' ){
		echo(iconv_UTF("정상적으로 글을 삭제하시기 바랍니다"));
		exit;
	}

  // 게시판 이름 지정이 안되어 있으면 경고;;;
  if(!$id){
		echo(iconv_UTF("게시판 이름을 지정해 주셔야 합니다.<br><br>예) zboard.php?id=이름"));
		exit;
	}

  // DB 연결
  if(!$connect) $connect=dbconn_m();

  // 현재 게시판 설정 읽어 오기
  $setup=get_table_attrib($id);

  // 설정되지 않은 게시판일때 에러 표시
  if(!$setup[name]){
		echo(iconv_UTF("생성되지 않은 게시판입니다.<br><br>게시판을 생성후 사용하십시요"));
		exit;
	}
  // 현재 게시판의 그룹의 설정 읽어 오기
  $group=group_info($setup[group_no]);

  // 멤버 정보 구해오기;;; 멤버가 있을때
  $member=member_info();

  // 현재 로그인되어 있는 멤버가 전체, 또는 그룹관리자인지 검사
  if($member[is_admin]==1||$member[is_admin]==2&&$member[group_no]==$setup[group_no]||check_board_master($member, $setup[no])) $is_admin=1; else $is_admin="";

  // 접근 금지 아이피인 경우 금지하기;;;
  $avoid_ip=explode(",",$setup[avoid_ip]);
  for($i=0;$i<count($avoid_ip);$i++)
  {
		if(!isblank($avoid_ip[$i])&&eregi($avoid_ip[$i],$REMOTE_ADDR)&&!$is_admin){
			echo(iconv_UTF(" Access Denied "));
			exit;
		}
  }

  // 현재 그룹이 폐쇄그룹이고 로그인한 멤버가 비멤버일때 에러표시
  if($group[is_open]==0&&!$is_admin&&$member[group_no]!=$setup[group_no]){
		echo(iconv_UTF("공개 되어 있지 않습니다"));
		exit;
	}

  //패스워드를 암호화
  if($password)
  {
   $temp=mysql_fetch_assoc(mysql_query("select password('$password') AS pass"));
   $password=$temp['pass'];   
  }

  // 원본글을 가져옴
  $s_data=mysql_fetch_assoc(mysql_query("select * from $t_board"."_$id where no='$no'"));

  // 회원일때를 확인;;
  if(!$is_admin&&$member[level]>$setup[grant_delete])
  {
   if(!$s_data[ismember])
   {
    if($s_data[password]!=$password){
			echo(iconv_UTF("비밀번호가 올바르지 않습니다"));
			exit;
		}
   }
   else
   {
    if($s_data[ismember]!=$member[no]){
			echo(iconv_UTF("비밀번호를 입력하여 주십시요"));
			exit;
		}
   }
